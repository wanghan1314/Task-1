package hzw.model;

/**
 * 优秀学员
 */

public class GreatStudent {
    private Integer sId;
    private String sName;
    private String sPicture;
    private String sProfession;
    private String sIntroduce;

    public Integer getsId() {
        return sId;
    }

    public void setsId(Integer sId) {
        this.sId = sId;
    }

    public String getsName() {
        return sName;
    }

    public void setsName(String sName) {
        this.sName = sName;
    }

    public String getsPicture() {
        return sPicture;
    }

    public void setsPicture(String sPicture) {
        this.sPicture = sPicture;
    }

    public String getsProfession() {
        return sProfession;
    }

    public void setsProfession(String sProfession) {
        this.sProfession = sProfession;
    }

    public String getsIntroduce() {
        return sIntroduce;
    }

    public void setsIntroduce(String sIntroduce) {
        this.sIntroduce = sIntroduce;
    }
}
